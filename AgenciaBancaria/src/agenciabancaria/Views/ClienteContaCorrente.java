    private void jButtonProsseguirPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProsseguirPagamentoActionPerformed
        // TODO add your handling code here:
        try{
            
            float valor = Float.parseFloat(jTextFieldValorPagamento.getText());
            int dia = Integer.parseInt(diaPagamento.getText());
            int mes = Integer.parseInt(mesPagamento.getText());
            int ano = Integer.parseInt(anoPagamento.getText());
            String descricao = jTextFieldDescPagamento.getText();
            String tipo = (String) jComboBoxTipoPagamento.getSelectedItem();
            tipo = tipo.toUpperCase();
            
            if(dia <= 0 || dia > 31 || mes <= 0 || mes > 12 || ano <= 0)
                  throw new NumberFormatException("Datas Erradas");
            
            float saldoAtual = Float.parseFloat(SaldoContaCorrente.getText());
            float diff = saldoAtual-valor;
            
            if(diff < 0){
                JOptionPane.showMessageDialog(null, "Saldo Insuficiente");
                LimparPagamento();
                return;
            }
                  
            Operacao_Bancaria op = new Operacao_Bancaria();
            op.setId_conta(ID_conta);
            op.setAgencia_nome(agencia_nome);
            op.setTipo(tipo);
            op.setValor(valor);
            op.setAno(ano);
            op.setMes(mes);
            op.setDia(dia);
            op.setDescricao(descricao);
            
            int ans = DBConnection.insere(op, "");
            
            if(ans != -1){
                JOptionPane.showMessageDialog(null, "Pagamento efetivado com sucesso");
                
                String sql = "UPDATE conta SET saldo=" + diff + " WHERE "
                        + "id_conta = " + ID_conta + " AND "
                        + "agencia_nome = '" + agencia_nome + "';";
               
                System.out.println(tipo);   
                if(tipo.equals("DEBITO")){
                    System.out.println("entrou");
                    DBConnection.atualiza(sql);
                    atualizaSaldo();
                }
                
            } else{
                JOptionPane.showMessageDialog(null, "Dados Incorretos");
                LimparPagamento();
            }
            
        } catch (Exception e) {
            LimparPagamento();
            JOptionPane.showMessageDialog(null, "Dados Incorretos");
            System.out.println("Algo deu errado " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonProsseguirPagamentoActionPerformed

