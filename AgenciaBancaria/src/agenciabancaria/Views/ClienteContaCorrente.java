    private void populaParticiparEmprestimo()
    {
        String sql = "SELECT C.id, C.nome, E.id_emprestimo, E.valor FROM " +
                    "(cliente C INNER JOIN" +
                    "   (emprestimo_cliente EC INNER JOIN emprestimo E " +
                            "ON EC.id_emprestimo = E.id_emprestimo) " +
                        "ON EC.id_cliente = C.id)";
      
        nomesClientesEmprestimos = new HashMap<>();
        valoresEmprestimos = new HashMap<>();
     
        jComboBoxNomeCliente.removeAllItems();
        
        indices = new HashMap<>();

        try{
            ResultSet resposta = DBConnection.seleciona(sql);
                        
            while(resposta.next())
            {
                int id = resposta.getInt("id");
                String nome = resposta.getString("nome");
                int id_emprestimo = resposta.getInt("id_emprestimo");
                Float valor = resposta.getFloat("valor");
                
                if(valoresEmprestimos.get(id) == null)
                    valoresEmprestimos.put(id, new ArrayList<>());
                
                nomesClientesEmprestimos.put(id, nome);
                valoresEmprestimos.get(id).add(new Pair<Integer, Float>(id_emprestimo, valor));
                
            }
            
            int indice = 0;
            Set<Integer> aux = nomesClientesEmprestimos.keySet();
            for(int id : aux){
                
                jComboBoxNomeCliente.addItem(nomesClientesEmprestimos.get(id));
                indices.put(indice, id);
                indice++;
            }
    
        } catch(Exception e){
            e.printStackTrace();
        }
    }
