    private void jButtonCadastraCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastraCCActionPerformed
        // TODO add your handling code here:
        try{
            String cpf = jTextFieldCPFCC.getText();
            float tarifa = Float.parseFloat(jTextFieldTarifaMensalCC.getText());
            int dia = Integer.parseInt(jTextFieldDiaCC.getText());
            int mes = Integer.parseInt(jTextFieldMesCC.getText());
            int ano = Integer.parseInt(jTextFieldAnoCC.getText());
            String agencia_nome = jTextFieldAgenciaCC.getText();
            
            Conta con = new Conta();
            con.setAgencia_nome(agencia_nome);
            con.setAno(ano);
            con.setDia(dia);
            con.setMes(mes);
            con.setSaldo(0);
            con.setTipo_conta("corrente");
            
            int ans = DBConnection.insere(con, "");
            
            if(ans != -1){
                
                String sql = "SELECT CURRVAL('Seq_Conta');";
                ResultSet resultado = DBConnection.seleciona(sql);
                int id_conta =0;
                while(resultado.next()){
                 id_conta = resultado.getInt("currval");
                }
                sql = "SELECT id FROM cliente WHERE cpf ='" + cpf + "';";
                resultado = DBConnection.seleciona(sql);
                int id_cliente = 0;
                while(resultado.next()){
                    id_cliente = resultado.getInt("id");
                }
                        
                Conta_Cliente concli = new Conta_Cliente();
                concli.setAgencia_nome(agencia_nome);
                concli.setId_cliente(id_cliente);
                concli.setId_conta(id_conta);
                ans = DBConnection.insere(concli, "");
                //}
                if(ans != -1){
                    Corrente corr = new Corrente();
                    corr.setAgencia_nome(agencia_nome);
                    corr.setId_conta(id_conta);
                    corr.setTarifa_mensal(tarifa);
                    ans = DBConnection.insere(corr, "");
                    
                    if(ans != -1)
                        JOptionPane.showMessageDialog(null, "Conta Criada com Sucesso");
                    else
                        JOptionPane.showMessageDialog(null, "Dados Incorretos");
                        
                } else {
                    JOptionPane.showMessageDialog(null, "Dados Incorretos");
                }
            }
            else
                JOptionPane.showMessageDialog(null, "Dados Incorretos");
            
        } catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Dados Incorretos");
            LimparCC();
        }
    }//GEN-LAST:event_jButtonCadastraCCActionPerformed

