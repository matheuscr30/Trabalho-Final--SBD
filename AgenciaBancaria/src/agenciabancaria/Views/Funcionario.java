    private void jButtonCadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarClienteActionPerformed
        // TODO add your handling code here:
        try{
            String nome = jTextFieldNomeCliente.getText();
            String cpf = jTextFieldCPFCliente.getText();
            int dia = Integer.parseInt(jTextFieldDiaCliente.getText());
            int mes = Integer.parseInt(jTextFieldMesCliente.getText());
            int ano = Integer.parseInt(jTextFieldAnoCliente.getText());
            String endereco = jTextFieldEnderecoCliente.getText();
            String estado = jTextFieldEstadoCliente.getText();
            String cidade = jTextFieldCidadeCliente.getText();
            Boolean ehGerente = jCheckBoxIsGerente.isSelected();
            
            if(dia <= 0 || dia > 31 || mes <= 0 || mes > 12 || ano <= 0 || cpf.length() != 11 || new Double(cpf).isNaN())
                  throw new NumberFormatException("Datas Erradas");
            
            Cliente cli = new Cliente();
            cli.setNome(nome);
            cli.setCpf(cpf);
            cli.setDia(dia);
            cli.setMes(mes);
            cli.setAno(ano);
            cli.setEndereco(endereco);
            cli.setEstado(estado);
            cli.setCidade(cidade);
            cli.setGerente( ehGerente == false ? -1 : NumFuncionario );
            int ans = DBConnection.insere(cli, "");
            
            if(ans != -1)
                JOptionPane.showMessageDialog(null, "Cliente Criado com Sucesso");
            else
                JOptionPane.showMessageDialog(null, "Dados Incorretos");
            
        } catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Dados Incorretos");
            LimparCliente();
        }
    }//GEN-LAST:event_jButtonCadastrarClienteActionPerformed

